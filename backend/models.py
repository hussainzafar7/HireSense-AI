class Interview(db.Model):
    __tablename__ = "interviews"
    id = db.Column(db.Integer, primary_key=True)
    candidate_id = db.Column(db.Integer, db.ForeignKey("candidates.id"), nullable=False)
    job_id = db.Column(db.Integer, db.ForeignKey("jobs.id"))
    resume_id = db.Column(db.Integer, db.ForeignKey("resumes.id"))
    total_questions = db.Column(db.Integer, default=0)
    answered_questions = db.Column(db.Integer, default=0)
    skipped_questions = db.Column(db.Integer, default=0)
    overall_score = db.Column(db.Float)
    communication_score = db.Column(db.Float)
    technical_score = db.Column(db.Float)
    confidence_score = db.Column(db.Float)
    feedback = db.Column(db.Text)
    strengths = db.Column(db.Text)
    weaknesses = db.Column(db.Text)
    recommendation = db.Column(db.String(50))
    recording_path = db.Column(db.String(500))
    transcript_path = db.Column(db.String(500))
    status = db.Column(db.String(20), default="in_progress")
    started_at = db.Column(db.DateTime, default=lambda: datetime.now(timezone.utc))
    completed_at = db.Column(db.DateTime)

    candidate = db.relationship("Candidate", backref="interviews", lazy=True)
    job = db.relationship("Job", backref="interviews", lazy=True)
    questions = db.relationship("InterviewQuestion", backref="interview", lazy=True)

class InterviewQuestion(db.Model):
    __tablename__ = "interview_questions"
    id = db.Column(db.Integer, primary_key=True)
